package 두번째날;

public class for문연습 {

	public static void main(String[] args) {
		int N = 5;
		// N번 반복한다.
		for(int i=0; i < N; i++) {
			System.out.println(i);
		}
		System.out.println("----------------");
		int a = 3; int b = 8;
		// a부터 b까지 반복한다.
		for(int i=a; i <= b; i++) {
			System.out.println(i);
		}
	}

}
